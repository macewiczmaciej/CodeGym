package en.codegym.task.jdk13.task04.task0437;

/* 
Triangle of eights
*/

public class Solution {
    public static void main(String[] args) {
        //write your code here
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print(8);
            }
            System.out.println();
        }
    }
}
